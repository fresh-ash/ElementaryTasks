package model;

public class NumberToWord {

    int number;
    String numberToWord;

    public NumberToWord(int number) {
        this.number = number;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getNumberToWord() {
        return numberToWord;
    }

    public void setNumberToWord(String numberToWord) {
        this.numberToWord = numberToWord;
    }
}
